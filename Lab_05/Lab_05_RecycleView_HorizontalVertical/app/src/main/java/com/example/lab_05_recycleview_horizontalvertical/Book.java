package com.example.lab_05_recycleview_horizontalvertical;

public class Book {

    private Integer id;
    private String name;
    private int image;

    public Book(){

    }

    public Book(Integer id, String name, int image) {
        this.id = id;
        this.name = name;
        this.image = image;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
